package tick.prasad.tick_lite.app;

public class MoreDetailsGS {

    private String place, price;

    public MoreDetailsGS(String place, String price) {
        this.place = place;
        this.price = price;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
